import pytest
from maths import maths

_test_maths = (
        ("1 + 2", 3),
        ("2 * 3", 6),
        ("1 + 2 * 3", 9),
        ("1 + 2 * 3 + 4", 21),
        ("1 + 2 * 3 + 4 * 5 + 6", 231),
        ("1 + (2 * 3) + (4 * (5 + 6))", 51),
        ("2 * 3 + (4 * 5)", 46),
        ("5 + (8 * 3 + 9 + 3 * 4 * 3)", 1445),
        ("5 * 9 * (7 * 3 * 3 + 9 * 3 + (8 + 6 * 4))", 669060),
        ("(2 + 4 * 9)", 54),
        ("(6 + 9 * 8 + 6)", 210),
        ("2 * (3 + 4) + 5", 24),
        ("(2 + 4 * 9) * (6 + 9 * 8 + 6) + 6", 11664),
        ("((2 + 4 * 9) * (6 + 9 * 8 + 6) + 6) + 2 + 4 * 2", 23340),
)

@pytest.mark.parametrize('n,expect', _test_maths)
def test_maths(n, expect):
    assert expect == maths(n)
